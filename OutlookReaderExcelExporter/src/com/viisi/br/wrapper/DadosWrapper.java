package com.viisi.br.wrapper;

public class DadosWrapper {

	private String dataInicio;
	private String dataFim;
	private String tempoProcessamento;
	private String cooperativa;
	private String periodoContabil;
	private String situacao;
	private String mensagemProcessamento;

	public String getDataInicio() {
		return dataInicio;
	}

	public void setDataInicio(String dataInicio) {
		this.dataInicio = dataInicio;
	}

	public String getDataFim() {
		return dataFim;
	}

	public void setDataFim(String dataFim) {
		this.dataFim = dataFim;
	}

	public String getTempoProcessamento() {
		return tempoProcessamento;
	}

	public void setTempoProcessamento(String tempoProcessamento) {
		this.tempoProcessamento = tempoProcessamento;
	}

	public String getCooperativa() {
		return cooperativa;
	}

	public void setCooperativa(String cooperativa) {
		this.cooperativa = cooperativa;
	}

	public String getPeriodoContabil() {
		return periodoContabil;
	}

	public void setPeriodoContabil(String periodoContabil) {
		this.periodoContabil = periodoContabil;
	}

	public String getSituacao() {
		return situacao;
	}

	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}

	public String getMensagemProcessamento() {
		return mensagemProcessamento;
	}

	public void setMensagemProcessamento(String mensagemProcessamento) {
		this.mensagemProcessamento = mensagemProcessamento;
	}

}
